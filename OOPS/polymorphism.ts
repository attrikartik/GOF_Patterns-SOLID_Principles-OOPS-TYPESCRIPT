/**
 * Polymorphism example in TypeScript
 * 
 * if we see abstraction and polymorphism closely, both are related to interfaces and classes.
 * Abstraction is about hiding the implementation details and showing only the essential features of the object.
 * Polymorphism is about using a single interface to represent different underlying forms (data types).
 * 
 * In the previous abstraction example, we defined an interface Shape and implemented it in Circle and Rectangle classes.
 * The functions calculateTotalArea and calculateTotalPerimeter demonstrate abstraction by hiding the implementation details.
 * 
 * Now, let's see how polymorphism works in this context. We can use the same interface Shape to refer to different types of shapes.
 * This allows us to write more flexible and reusable code.     
 */